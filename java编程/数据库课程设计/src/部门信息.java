public class 部门信息 {
    private int D_NUMBER;
    private String D_NAME;
    private int D_COUNT;
    public int getNumber()
    {
        return D_NUMBER;
    }
    public String getName()
    {
        return D_NAME;
    }
    public int getCount()
    {
        return D_COUNT;
    }
    public void setNumber(int number)
    {
        D_NUMBER=number;
    }
    public void setName(String name)
    {
        D_NAME=name;
    }
    public void setCount(int count)
    {
        D_COUNT=count;
    }
}
